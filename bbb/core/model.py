"""
Модуль model — правые части ОДУ двухкомпартментной модели ГЭБ.

Математическая постановка
-------------------------
Рассматривается система из двух компартментов:
  - Апикальный (кровь): концентрация C_blood, объём V_blood
  - Базолатеральный (мозг): концентрация C_brain, объём V_brain

Транспортные механизмы:
  1. Пассивная диффузия — описывается законом Фика:
         J_diff = k_pass * (C_blood - C_brain)   [мкМ/ч]
     где k_pass — эффективный коэффициент пассивной проницаемости [1/ч],
     нормированный на площадь мембраны и объём компартмента.

  2. Активный эффлюкс (P-гликопротеин) — кинетика Михаэлиса–Ментен:
         J_pgp = Vmax * C_brain / (Km + C_brain)   [мкМ/ч]
     где Vmax — максимальная скорость транспортёра [мкМ/ч],
         Km   — константа Михаэлиса (концентрация полунасыщения) [мкМ].
     P-gp перекачивает вещество из мозга обратно в кровь.

Система ОДУ:
  dC_blood/dt = -J_diff + (V_brain / V_blood) * J_pgp
  dC_brain/dt = (V_blood / V_brain) * J_diff - J_pgp

Коэффициент (V_brain / V_blood) обеспечивает закон сохранения вещества:
количество молекул, покидающих один компартмент, равно количеству,
поступающих в другой.
"""

import numpy as np


def bbb_ode(
    t: float,
    y: list[float],
    k_pass: float,
    vmax: float,
    km: float,
    v_blood: float,
    v_brain: float,
) -> list[float]:
    """
    Правые части системы ОДУ двухкомпартментной модели ГЭБ.

    Параметры
    ---------
    t : float
        Текущее время [ч]. Уравнения автономны, параметр требуется
        solve_ivp по соглашению.
    y : list[float]
        Вектор состояния [C_blood, C_brain] — концентрации [мкМ].
    k_pass : float
        Коэффициент пассивной проницаемости [1/ч].
    vmax : float
        Максимальная скорость P-gp эффлюкса [мкМ/ч].
    km : float
        Константа Михаэлиса для P-gp [мкМ].
    v_blood : float
        Относительный объём кровяного компартмента [усл. ед.].
    v_brain : float
        Относительный объём мозгового компартмента [усл. ед.].

    Возвращает
    ----------
    list[float]
        [dC_blood/dt, dC_brain/dt] — производные концентраций [мкМ/ч].
    """
    c_blood, c_brain = y

    # Пассивная диффузия (закон Фика): направление кровь → мозг
    j_diff = k_pass * (c_blood - c_brain)

    # Активный эффлюкс P-gp (Михаэлис–Ментен): направление мозг → кровь
    # Защита от отрицательных концентраций (численные артефакты)
    c_brain_safe = max(c_brain, 0.0)
    j_pgp = vmax * c_brain_safe / (km + c_brain_safe)

    # Коэффициент пересчёта объёмов
    volume_ratio = v_brain / v_blood

    dC_blood_dt = -j_diff + volume_ratio * j_pgp
    dC_brain_dt = (1.0 / volume_ratio) * j_diff - j_pgp

    return [dC_blood_dt, dC_brain_dt]
