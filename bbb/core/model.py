"""
Модуль model — правые части ОДУ двухкомпартментной модели ГЭБ.

Математическая постановка
-------------------------
Система из двух хорошо перемешанных компартментов:
  - Апикальный (кровь): концентрация C_blood, объём V_blood = 1.0 усл. ед.
  - Базолатеральный (мозг): концентрация C_brain, объём V_brain = 0.3 усл. ед.

Транспортные механизмы
----------------------
1. Пассивная диффузия (закон Фика):
       J_diff = k_pass * (C_blood - C_brain)   [мкМ/ч]
   k_pass — эффективный коэффициент проницаемости [1/ч],
   нормированный на площадь мембраны и объём компартмента.

2. Активный эффлюкс P-гп (Михаэлис–Ментен):
       J_pgp = Vmax * C_brain / (Km + C_brain)   [мкМ/ч]
   Vmax — максимальная скорость транспортёра [мкМ/ч],
   Km   — константа Михаэлиса [мкМ].
   P-gp направлен из мозга в кровь.

Система ОДУ
-----------
  dC_blood/dt = -J_diff + (V_brain / V_blood) * J_pgp
  dC_brain/dt = (V_blood / V_brain) * J_diff - J_pgp

Объёмные коэффициенты гарантируют закон сохранения вещества:
  d/dt (C_blood * V_blood + C_brain * V_brain) = 0

Численное интегрирование реализовано в bbb.core.simulator.Simulator.
"""

def bbb_ode(
    t: float,
    y: list[float],
    k_pass: float,
    vmax: float,
    km: float,
    v_blood: float,
    v_brain: float,
    k_elim: float = 0.0,
) -> list[float]:
    """
    Правые части системы ОДУ двухкомпартментной модели ГЭБ.

    Параметры
    ---------
    t : float
        Текущее время [ч]. Уравнения автономны, параметр требуется
        solve_ivp по соглашению.
    y : list[float]
        Вектор состояния [C_blood, C_brain] — концентрации [мкМ].
    k_pass : float
        Коэффициент пассивной проницаемости [1/ч].
    vmax : float
        Максимальная скорость P-gp эффлюкса [мкМ/ч].
    km : float
        Константа Михаэлиса для P-gp [мкМ].
    v_blood : float
        Относительный объём кровяного компартмента [усл. ед.].
    v_brain : float
        Относительный объём мозгового компартмента [усл. ед.].
    k_elim : float
        Константа системного клиренса из крови [1/ч] (k_elim = CL / V_blood).
        При k_elim = 0 модель соответствует закрытой системе (in vitro).

    Возвращает
    ----------
    list[float]
        [dC_blood/dt, dC_brain/dt] — производные концентраций [мкМ/ч].
    """
    c_blood, c_brain = y
    c_blood_safe = max(c_blood, 0.0)

    # Пассивная диффузия (закон Фика): направление кровь → мозг
    j_diff = k_pass * (c_blood_safe - c_brain)

    # Активный эффлюкс P-gp (Михаэлис–Ментен): направление мозг → кровь
    # Защита от отрицательных концентраций (численные артефакты)
    c_brain_safe = max(c_brain, 0.0)
    j_pgp = vmax * c_brain_safe / (km + c_brain_safe)

    # Системный клиренс из крови (печень + почки; 0 в режиме in vitro)
    j_elim = k_elim * c_blood_safe

    # Коэффициент пересчёта объёмов
    volume_ratio = v_brain / v_blood

    dC_blood_dt = -j_diff + volume_ratio * j_pgp - j_elim
    dC_brain_dt = (1.0 / volume_ratio) * j_diff - j_pgp

    return [dC_blood_dt, dC_brain_dt]
