def get_substance(name: str) -> Substance:
    """
    Получить вещество из реестра по названию.

    Параметры
    ---------
    name : str
        Название вещества (должно совпадать с ключом в SUBSTANCES).

    Возвращает
    ----------
    Substance
        Объект с параметрами вещества.

    Исключения
    ----------
    KeyError
        Если вещество с данным названием не найдено в реестре.
    """
    if name not in SUBSTANCES:
        available = ", ".join(SUBSTANCES.keys())
        raise KeyError(
            f"Вещество '{name}' не найдено в реестре. "
            f"Доступные вещества: {available}."
        )
    return SUBSTANCES[name]
