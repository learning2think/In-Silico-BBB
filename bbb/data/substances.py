"""
Модуль substances — реестр модельных веществ для симуляции ГЭБ.

Каждое вещество описывается датаклассом Substance, содержащим
биофизические параметры транспортной модели и метаданные для отображения.

Предустановленные вещества
--------------------------
Параметры подобраны на основе литературных данных и адаптированы
для безразмерной двухкомпартментной модели (k_pass в [1/ч],
Vmax в [мкМ/ч], Km в [мкМ]):

  Диазепам   — высоколипофильный препарат, хорошо проникает через ГЭБ.
               Слабый субстрат P-gp. Референс: Hammarlund-Udenaes et al.
  Верапамил  — умеренная липофильность, выраженный субстрат P-gp.
               Референс: Choo et al. (2006), Johansson et al. (2005).
  Декстран   — крупная гидрофильная молекула, практически не проходит ГЭБ.
               Используется как экспериментальный маркер целостности барьера.
  Кофеин     — малая молекула со средней проницаемостью, не субстрат P-gp.
               Референс: Fredholm et al. (1999).

Добавление нового вещества
--------------------------
Создайте экземпляр Substance и добавьте его в словарь SUBSTANCES:

    SUBSTANCES["Новое вещество"] = Substance(
        name="Новое вещество",
        k_pass=0.5,
        vmax=1.0,
        km=20.0,
        color="#FF5733",
        description="Краткое описание вещества.",
        reference="Автор и год.",
    )
"""

from dataclasses import dataclass, field


@dataclass
class Substance:
    """
    Параметры вещества для двухкомпартментной модели ГЭБ.

    Атрибуты
    --------
    name : str
        Название вещества (отображается в интерфейсе).
    k_pass : float
        Коэффициент пассивной проницаемости [1/ч].
        Характеризует скорость пассивной диффузии через липидный бислой.
    vmax : float
        Максимальная скорость P-gp эффлюкса [мкМ/ч].
        0.0 означает, что вещество не является субстратом P-gp.
    km : float
        Константа Михаэлиса для P-gp [мкМ].
        Концентрация, при которой скорость эффлюкса равна Vmax / 2.
    color : str
        Цвет линии на графике (HEX-код).
    description : str
        Краткое описание вещества для интерфейса.
    reference : str
        Источник литературных значений параметров.
    """

    name: str
    k_pass: float           # [1/ч]
    vmax: float             # [мкМ/ч]
    km: float               # [мкМ]
    color: str = "#1f77b4"
    description: str = ""
    reference: str = ""


