# ---------------------------------------------------------------------------
# Конфигурация страницы
# ---------------------------------------------------------------------------

st.set_page_config(
    page_title="Цифровой двойник ГЭБ",
    page_icon="🧠",
    layout="wide",
    initial_sidebar_state="expanded",
)


# ---------------------------------------------------------------------------
# Вспомогательные функции (объявляются до использования в теле страницы)
# ---------------------------------------------------------------------------

def _render_conclusion(
    kp_uu: float,
    c_brain_max: float,
    params: SimulationParams,
) -> None:
    """
    Вывести текстовое заключение о проницаемости вещества через ГЭБ.

    Классификация основана на значении Kp,uu — отношении несвязанных
    концентраций мозг/кровь в квазиравновесии:
      - Kp,uu ≥ 0.8 → высокая проницаемость (пассивное равновесие)
      - 0.3 ≤ Kp,uu < 0.8 → умеренная проницаемость
      - Kp,uu < 0.3 → низкая проницаемость (выраженный эффлюкс или барьер)

    Параметры
    ---------
    kp_uu : float
        Отношение концентраций мозг/кровь в квазиравновесии.
    c_brain_max : float
        Максимальная концентрация в мозге [мкМ].
    params : SimulationParams
        Параметры симуляции (для контекстных комментариев).
    """
    if kp_uu >= 0.8:
        level = "🟢 Высокая"
        verdict = (
            "Вещество хорошо проникает через ГЭБ. Концентрации в мозге и крови "
            "приближаются к равновесию. Активный эффлюкс выражен слабо или отсутствует."
        )
    elif kp_uu >= 0.3:
        level = "🟡 Умеренная"
        verdict = (
            "Вещество частично проникает через ГЭБ. Наблюдается умеренный активный "
            "эффлюкс (P-gp), снижающий накопление в ткани мозга."
        )
    else:
        level = "🔴 Низкая"
        verdict = (
            "Вещество плохо проникает через ГЭБ. Выраженный P-gp эффлюкс и/или "
            "низкая пассивная проницаемость существенно ограничивают концентрацию "
            "в ткани мозга."
        )
    
    st.markdown(
        f"""
        | Показатель | Значение |
        |---|---|
        | **Уровень проницаемости** | {level} |
        | **Kp,uu (мозг/кровь)** | {kp_uu:.3f} |
        | **Cmax в мозге** | {c_brain_max:.4f} мкМ |
        | **k_pass** | {params.k_pass:.3f} 1/ч |
        | **Vmax (P-gp)** | {params.vmax:.2f} мкМ/ч |
        | **Km (P-gp)** | {params.km:.1f} мкМ |

        **Вывод:** {verdict}
        """
    )

# ---------------------------------------------------------------------------
# Заголовок и краткое описание
# ---------------------------------------------------------------------------

st.title("🧠 Цифровой двойник гематоэнцефалического барьера")
st.markdown(
    """
    **In Silico BBB** — вычислительная платформа для моделирования транспорта
    лекарственных веществ через гематоэнцефалический барьер (ГЭБ).

    Модель реализует двухкомпартментный подход:
    * **Апикальный компартмент** (кровь) — источник вещества.
    * **Базолатеральный компартмент** (мозг) — целевая ткань.

    Транспортные механизмы:
    * 🔵 **Пассивная диффузия** — движение вещества по градиенту концентрации
      (закон Фика), скорость определяется параметром *k_pass*.
    * 🔴 **Активный эффлюкс P-gp** — АТФ-зависимый транспортёр, откачивающий
      вещества из мозга обратно в кровь; кинетика описывается уравнением
      Михаэлиса–Ментен (*Vmax* и *Km*).
    """
)

# ---------------------------------------------------------------------------
# Боковая панель управления
# ---------------------------------------------------------------------------

with st.sidebar:
    st.header("⚙️ Параметры симуляции")

    # --- Выбор вещества ---
    st.subheader("Вещество")
    substance_names = list(SUBSTANCES.keys())
    selected_name = st.selectbox(
        "Выберите вещество из реестра:",
        options=substance_names,
        index=0,
    )
    substance = SUBSTANCES[selected_name]

    # Показать описание вещества
    st.info(
        f"**{substance.name}**\n\n"
        f"{substance.description}\n\n"
        f"*Источник: {substance.reference}*"
    )

    st.divider()

    # --- Параметры модели ---
    st.subheader("Параметры транспорта")

    k_pass = st.slider(
        label="k_pass — пассивная проницаемость [1/ч]",
        min_value=0.001,
        max_value=2.0,
        value=float(substance.k_pass),
        step=0.001,
        format="%.3f",
        help=(
            "Коэффициент пассивной диффузии через мембрану ГЭБ. "
            "Высокие значения характерны для липофильных молекул."
        ),
    )

    vmax = st.slider(
        label="Vmax — максимальная скорость P-gp [мкМ/ч]",
        min_value=0.0,
        max_value=10.0,
        value=float(substance.vmax),
        step=0.05,
        format="%.2f",
        help=(
            "Максимальная скорость эффлюкса P-гликопротеина. "
            "0 = вещество не является субстратом P-gp."
        ),
    )

    km = st.slider(
        label="Km — константа Михаэлиса [мкМ]",
        min_value=0.1,
        max_value=100.0,
        value=float(substance.km),
        step=0.1,
        format="%.1f",
        help=(
            "Концентрация в мозге, при которой скорость P-gp равна Vmax/2. "
            "Малые значения Km означают высокое сродство транспортёра."
        ),
    )

    st.divider()

    # --- Начальные условия и время ---
    st.subheader("Начальные условия")

    c0_blood = st.number_input(
        label="C₀ в крови [мкМ]",
        min_value=0.01,
        max_value=100.0,
        value=1.0,
        step=0.1,
        format="%.2f",
        help="Начальная концентрация вещества в кровяном компартменте.",
    )

    t_end = st.slider(
        label="Время симуляции [ч]",
        min_value=1,
        max_value=72,
        value=24,
        step=1,
        help="Длительность моделирования.",
    )

    st.divider()

    # --- Кнопка запуска ---
    run_button = st.button(
        "▶ Запустить расчёт",
        type="primary",
        use_container_width=True,
    )

# ---------------------------------------------------------------------------
# Вкладки основного контента
# ---------------------------------------------------------------------------

tab_results, tab_model, tab_about = st.tabs(
    ["📊 Результаты", "📐 Математическая модель", "ℹ️ О проекте"]
)

# ---- Вкладка «Результаты» -------------------------------------------------

with tab_results:
    if run_button or "last_result" in st.session_state:
        if run_button:
            # Сборка параметров и запуск симуляции
            params = SimulationParams(
                k_pass=k_pass,
                vmax=vmax,
                km=km,
                c0_blood=c0_blood,
                c0_brain=0.0,
                t_end=float(t_end),
                v_blood=1.0,
                v_brain=0.3,
                n_points=600,
            )

            with st.spinner("Выполняется численное интегрирование..."):
                simulator = Simulator()
                result = simulator.run(params)

            # Сохраняем результат в session_state
            st.session_state["last_result"] = result
            st.session_state["last_params"] = params
            st.session_state["last_substance"] = selected_name

        # Извлечение из session_state
        result = st.session_state["last_result"]
        params = st.session_state["last_params"]
        substance_name_used = st.session_state.get("last_substance", selected_name)

        if not result.success:
            st.error(f"Решатель завершился с ошибкой: {result.message}")
        else:
            # --- Метрики ---
            col1, col2, col3 = st.columns(3)
            col1.metric(
                "Cmax в мозге",
                f"{result.c_brain_max:.4f} мкМ",
                help="Максимальная концентрация в мозговом компартменте.",
            )
            col2.metric(
                "AUC в мозге",
                f"{result.auc_brain:.2f} мкМ·ч",
                help="Площадь под кривой концентрация-время в мозге.",
            )
            # Отношение равновесных концентраций (последние 5% времени)
            n_tail = max(int(len(result.t) * 0.05), 1)
            kp_uu = (
                result.c_brain[-n_tail:].mean()
                / result.c_blood[-n_tail:].mean()
            )
            col3.metric(
                "Kp,uu (равновесие)",
                f"{kp_uu:.3f}",
                help=(
                    "Отношение несвязанных концентраций мозг/кровь "
                    "в квазиравновесии. Kp,uu < 1 указывает на активный эффлюкс."
                ),
            )

            # --- График ---
            fig = plot_concentrations(
                result=result,
                substance_name=substance_name_used,
                blood_color="#E53935",
                brain_color="#1E88E5",
                show_auc=True,
            )
            st.pyplot(fig)

            # --- Текстовое заключение ---
            st.subheader("📋 Заключение о проницаемости")
            _render_conclusion(kp_uu, result.c_brain_max, params)

    else:
        st.info(
            "Настройте параметры в боковой панели и нажмите "
            "**▶ Запустить расчёт** для начала симуляции."
        )
        # Демонстрационное изображение с подсказкой
        st.markdown(
            """
            #### Как пользоваться приложением:
            1. Выберите вещество из реестра или задайте параметры вручную.
            2. При необходимости скорректируйте *k_pass*, *Vmax*, *Km* и время симуляции.
            3. Нажмите **▶ Запустить расчёт**.
            4. Изучите график и автоматически сгенерированное заключение.
            """
        )

# ---- Вкладка «Математическая модель» -------------------------------------

with tab_model:
    st.markdown(
        r"""
        ## Математическая модель

        ### Компартменты
        Система состоит из двух хорошо перемешанных компартментов:

        | Компартмент | Обозначение | Объём (усл. ед.) |
        |---|---|---|
        | Кровь (апикальный) | $C_\text{blood}$ | $V_\text{blood} = 1.0$ |
        | Мозг (базолатеральный) | $C_\text{brain}$ | $V_\text{brain} = 0.3$ |

        ### Транспортные потоки

        **1. Пассивная диффузия** (закон Фика):
        $$J_\text{diff} = k_\text{pass} \cdot (C_\text{blood} - C_\text{brain})$$

        **2. Активный эффлюкс P-gp** (Михаэлис–Ментен):
        $$J_\text{P-gp} = \frac{V_\text{max} \cdot C_\text{brain}}{K_m + C_\text{brain}}$$

        ### Система ОДУ

        $$\frac{dC_\text{blood}}{dt} = -J_\text{diff} + \frac{V_\text{brain}}{V_\text{blood}} \cdot J_\text{P-gp}$$

        $$\frac{dC_\text{brain}}{dt} = \frac{V_\text{blood}}{V_\text{brain}} \cdot J_\text{diff} - J_\text{P-gp}$$

        Коэффициенты объёмов обеспечивают **закон сохранения вещества**:
        количество молекул, покидающих один компартмент, в точности равно
        числу молекул, поступающих в другой.

        ### Численный метод

        Система решается методом **LSODA** (`scipy.integrate.solve_ivp`).
        LSODA автоматически переключается между схемами Adams (нежёсткие участки)
        и BDF (жёсткие участки), что делает его оптимальным при неизвестной
        жёсткости системы — характерной ситуации при высоких концентрациях
        и малых значениях $K_m$.

        ### Параметры модели

        | Параметр | Обозначение | Единицы | Диапазон |
        |---|---|---|---|
        | Пассивная проницаемость | $k_\text{pass}$ | 1/ч | 0.001 – 2.0 |
        | Макс. скорость P-gp | $V_\text{max}$ | мкМ/ч | 0.0 – 10.0 |
        | Константа Михаэлиса | $K_m$ | мкМ | 0.1 – 100 |
        """
    )

